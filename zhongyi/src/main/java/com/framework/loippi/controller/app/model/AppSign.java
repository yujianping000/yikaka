package com.framework.loippi.controller.app.model;

public class AppSign {
	      private String avatar;
	      private String orgName;
	      private String userName;
	      private String currentTime;
	      private Integer enableSign;
	      private String signAddr;
	
      public String getAvatar() {
		return avatar;
	}
	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}
	public String getOrgName() {
		return orgName;
	}
	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getCurrentTime() {
		return currentTime;
	}
	public void setCurrentTime(String currentTime) {
		this.currentTime = currentTime;
	}
	public Integer getEnableSign() {
		return enableSign;
	}
	public void setEnableSign(Integer enableSign) {
		this.enableSign = enableSign;
	}
	public String getSignAddr() {
		return signAddr;
	}
	public void setSignAddr(String signAddr) {
		this.signAddr = signAddr;
	}
   
}
